#!/usr/bin/env node

const cdk = require('@aws-cdk/core');
const { ModulloCreatePipelineAwsStack } = require('../lib/modullo-create-pipeline-aws-stack');

const app = new cdk.App();

let modulloParamsPath = path.join(__dirname, `modullo-params.yaml`); // Autogenerated modullo parameters
let modulloParamsExists = await utilities.file_exists(modulloParamsPath);
if (modulloParamsExists) {
  let modulloParams = await utilities.readFile(options, "yaml", modulloParamsPath);

  new ModulloCreatePipelineAwsStack(app, 'ModulloCreatePipelineAwsStack', {
    /* If you don't specify 'env', this stack will be environment-agnostic.
    * Account/Region-dependent features and context lookups will not work,
    * but a single synthesized template can be deployed anywhere. */

    /* Uncomment the next line to specialize this stack for the AWS Account
    * and Region that are implied by the current CLI configuration. */
    // env: { account: process.env.CDK_DEFAULT_ACCOUNT, region: process.env.CDK_DEFAULT_REGION },

    /* Uncomment the next line if you know exactly what Account and Region you
    * want to deploy the stack to. */
    env: { account: modulloParams["pipeline-aws-account"], region: modulloParams["pipeline-aws-region"] },

    /* For more information, see https://docs.aws.amazon.com/cdk/latest/guide/environments.html */
  });

  app.synth();


}